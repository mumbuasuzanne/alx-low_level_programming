#ifndef ABS_MACRO_H
#define ABS_MACRO_H

/**
 * ABS - Compute the absolute value of a number.
 * @x: The number for which the absolute value is to be computed.
 *
 * Description: This macro computes the absolute value of a given number 'x'.
 *
 * Return: The absolute value of 'x'.
 */
#define ABS(x) ((x) < 0 ? -(x) : (x))

#endif /* ABS_MACRO_H */

